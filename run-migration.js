const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const prismaBuild = path.join('D:/Sai saas', 'node_modules', 'prisma', 'build', 'index.js');
const schema = path.join('D:/Sai saas', 'packages', 'database', 'prisma', 'schema.prisma');
const nodeExe = path.join('D:/Sai saas', 'node', 'node.exe');
const migrationsDir = path.join('D:/Sai saas', 'packages', 'database', 'prisma', 'migrations');

function run(cmd) {
  return execSync(cmd, { cwd: 'D:/Sai saas', env: process.env, timeout: 120000, stdio: 'pipe' });
}

async function main() {
  console.log('🔍 Step 1: Creating baseline migration folder...');
  
  // Create migrations directory and baseline folder
  const baselineName = '0001_baseline_init';
  const baselineDir = path.join(migrationsDir, baselineName);
  fs.mkdirSync(baselineDir, { recursive: true });

  // Create an empty migration.sql (baseline — tells Prisma "DB already has this")
  fs.writeFileSync(path.join(baselineDir, 'migration.sql'), '-- Baseline migration: DB already exists\n-- Tables were created manually or via a previous deploy\n');
  
  console.log('✅ Created baseline migration:', baselineName);

  // Create the migrations lock file
  const lockContent = `# Please do not edit this file manually
# It should be added in your version-control system (e.g., Git)

provider = "postgresql"
`;
  fs.writeFileSync(path.join(migrationsDir, 'migration_lock.toml'), lockContent);
  console.log('✅ Created migration_lock.toml');

  console.log('\n🔄 Step 2: Marking baseline as applied (resolve command)...');
  try {
    const resolveResult = run(
      `"${nodeExe}" "${prismaBuild}" migrate resolve --applied "${baselineName}" --schema="${schema}"`
    );
    console.log(resolveResult.toString());
    console.log('✅ Baseline marked as applied in Supabase!');
  } catch (e) {
    const stderr = e.stderr?.toString() || '';
    const stdout = e.stdout?.toString() || '';
    console.log('STDOUT:', stdout);
    console.log('STDERR:', stderr);
    
    // If already resolved, that's fine
    if (stderr.includes('already') || stdout.includes('already')) {
      console.log('✅ Already resolved!');
    }
  }

  console.log('\n🔄 Step 3: Running migrate deploy to apply any new changes...');
  try {
    const deployResult = run(
      `"${nodeExe}" "${prismaBuild}" migrate deploy --schema="${schema}"`
    );
    console.log(deployResult.toString());
    console.log('✅ Migration deploy complete!');
  } catch (e) {
    const stderr = e.stderr?.toString() || '';
    const stdout = e.stdout?.toString() || '';
    console.log('STDOUT:', stdout);
    console.error('STDERR:', stderr);
  }

  console.log('\n🔄 Step 4: Running db push to sync latest schema changes...');
  try {
    const pushResult = run(
      `"${nodeExe}" "${prismaBuild}" db push --schema="${schema}" --accept-data-loss`
    );
    console.log(pushResult.toString());
    console.log('✅ Schema synced to Supabase!');
  } catch (e) {
    const stderr = e.stderr?.toString() || '';
    const stdout = e.stdout?.toString() || '';
    console.log('STDOUT:', stdout);
    console.error('STDERR:', stderr);
  }
}

main().catch(console.error);
